#range(5) => loop will start from 0 and goes till 5 - 1 with inc of 1
for i in range(5):
    print(i)
print("------------")

#range(5,40) => loop will start from 5 and goes till 40 - 1 with inc of 1
for i in range(5,40):
    print(i)
print("------------")

# range(5,51,5) => loop will start from 5 and goes till 51 - 5 with inc of 5
for i in range(5,51,5):
    print(i)
print("------------")

for i in range(10,1,-1):
    print(i)
print("------------")

for i in reversed(range(1,10)):
    print(i)
